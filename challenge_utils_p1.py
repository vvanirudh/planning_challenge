"""
challenge_utils_p1 -- prebuilt helpers for the 30-minute Rover planning challenge.

GENERATED by build_notebooks_p1.py -- do not hand-edit. Loads the terrain data
and defines the visualizer, the official metric, and a COMPLETE A* planner
(`astar_plan`) so the candidate only has to write `cost_map_learned`. The true
terrain cost stays hidden behind `path_cost` / `evaluate` (it lives in
meta['true_cost'] as an opaque grid); recovering it from the demos is the point,
so resist peeking here.

The terrain has only mud and slope -- there are no rocks / impassable cells, so
every cell is traversable at a finite cost.

Convention: all points are (row, col), so a path point p indexes a cost map
directly as cost_map[p[0], p[1]]. features[r, c] is the 2-vector at that cell.
"""
