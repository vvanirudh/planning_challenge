        ax.plot(d[:, 1], d[:, 0], '0.7', lw=1, alpha=.7)
        ax.plot(d[0, 1],  d[0, 0],  'o', color='0.5', ms=3, alpha=.8)
        ax.plot(d[-1, 1], d[-1, 0], '*', color='0.5', ms=5, alpha=.8)
